#include "../includes/libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (lst == NULL || (*del) == NULL)
		return ;
	(*del)(lst -> content);
	lst -> next = NULL;
	free(lst);
}
