#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sum;
	int	is_negative;

	i = 0;
	sum = 0;
	is_negative = 1;
	while (str[i] == ' ' || (9 <= str[i] && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			is_negative = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = sum * 10 + (str[i] - '0');
		i++;
	}
	return (sum * is_negative);
}
